package app_01_primitive_data_types;

/**
 * Демонстрация примитивных типов данных в Java
 * 
 * Этот класс показывает основные примитивные типы данных:
 * - Целочисленные типы (byte, short, int, long)
 * - Типы с плавающей точкой (double, float)
 * - Символьный тип (char)
 * - Логический тип (boolean)
 */
public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // ============================================
        // ЦЕЛОЧИСЛЕННЫЕ ТИПЫ ДАННЫХ
        // ============================================
        
        // byte: 8-битное целое число, диапазон от -128 до 127
        byte a_1 = 15;
        
        // short: 16-битное целое число, диапазон от -32,768 до 32,767
        short a_2 = 15;
        
        // int: 32-битное целое число, диапазон от -2^31 до 2^31-1 (наиболее часто используемый)
        int a_3 = 15;
        
        // long: 64-битное целое число, диапазон от -2^63 до 2^63-1
        // Суффикс 'L' указывает, что это литерал типа long
        long a_4 = 15L;

        // ============================================
        // ТИПЫ С ПЛАВАЮЩЕЙ ТОЧКОЙ (ДРОБНЫЕ ЧИСЛА)
        // ============================================
        
        // double: 64-битное число с плавающей точкой (двойная точность)
        // По умолчанию дробные числа в Java имеют тип double
        double b_1 = 14.2;
        
        // float: 32-битное число с плавающей точкой (одинарная точность)
        // Суффикс 'F' указывает, что это литерал типа float
        float b_2 = 15.3F;

        // ============================================
        // СИМВОЛЬНЫЙ ТИП
        // ============================================
        
        // char: 16-битный символ Unicode, хранит один символ в одинарных кавычках
        char c_1 = 't';

        // ============================================
        // ЛОГИЧЕСКИЙ ТИП
        // ============================================
        
        // boolean: логический тип, может быть только true или false
        boolean d_1 = false;

        // ============================================
        // ВЫВОД ЗНАЧЕНИЙ В КОНСОЛЬ
        // ============================================
        
        // Вывод целочисленных типов
        System.out.println(a_1); // Вывод: 15
        System.out.println(a_2); // Вывод: 15
        System.out.println(a_3); // Вывод: 15
        System.out.println(a_4); // Вывод: 15

        // Вывод типов с плавающей точкой
        System.out.println(b_1); // Вывод: 14.2
        System.out.println(b_2); // Вывод: 15.3

        // Вывод символьного типа
        System.out.println(c_1); // Вывод: t

        // Вывод логического типа
        System.out.println(d_1); // Вывод: false
    }
}
