package app_02_variables;

/**
 * Демонстрация работы с переменными и различными операциями в Java
 * 
 * Этот класс показывает примеры использования:
 * - Арифметических операций присваивания
 * - Операций инкремента и декремента
 * - Приведения типов
 * - Работы с системными свойствами
 */
public class Variables {
    /**
     * Главный метод программы
     * 
     * @param args аргументы командной строки
     */
    public static void main(String[] args) {
        // Инициализация переменной целого типа
        int a = 5;
        System.out.println("Начальное значение a = " + a);
        
        // Деление с присваиванием: a = a / 2
        a /= 2;
        System.out.println("После a /= 2: a = " + a);
        
        // Умножение с присваиванием: a = a * 2
        a *= 2;
        System.out.println("После a *= 2: a = " + a);
        
        // Вычитание с присваиванием: a = a - 1
        a -= 1;
        System.out.println("После a -= 1: a = " + a);
        
        // Остаток от деления с присваиванием: a = a % 2
        a %= 2;
        System.out.println("После a %= 2: a = " + a);
        
        // Сложение с присваиванием: a = a + 5
        a += 5;
        System.out.println("После a += 5: a = " + a);
        
        // Постфиксный инкремент: увеличивает значение на 1 после использования
        a++;
        System.out.println("После a++: a = " + a);
        
        // Постфиксный декремент: уменьшает значение на 1 после использования
        a--;
        System.out.println("После a--: a = " + a);
        
        // Деление с приведением типа к double для получения дробного результата
        // (double) - явное приведение типа int к double
        double result = a / (double) 4;
        System.out.println("\nРезультат деления a / 4.0 = " + result);
        
    }
}
